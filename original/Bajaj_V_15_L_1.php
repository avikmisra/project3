<p class="par">
The Bajaj V15 is the latest 150cc commuter motorcycle from Bajaj Auto. A homage to heritage and history, the V15 gets metal from the scrapped aircraft carrier INS Vikrant. In a way, that in itself is the USP of this bike, named after India's first aircraft carrier - and marketed as a tough as nails, bold, sturdy new commuter motorcycle.</p>
<p class="par">

It's also a bold new attempt by Bajaj to bolster its fortunes in the 150cc commuter motorcycle space. Sure, the Pulsar 150 has always been the king of the 150cc segment, but other attempts by Bajaj to get a slice of the commuter riding populace have not met with much success - like the Discover 150 series. The V15 will straddle that space - bang in the middle of the Discover and the Pulsar series. So, it tries not to be a plain Jane commuter, yet doesn't aspire to be a sporty 150cc either.

