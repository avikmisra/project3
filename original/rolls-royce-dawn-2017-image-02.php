	<p><h2>&nbsp;Overview</h2></p>
	<p class="par">Luxurious, exclusive, exquisite: take your pick of adjectives for the lovely Dawn. Basically a Wraith coupe under the skin, the Dawn has sheetmetal that curves and swoops, making it—according to Rolls, but we don't necessarily disagree—the "sexiest Rolls-Royce ever built." Inside, there is room for four adult passengers; occupants will be dazzled by the gorgeous leather and acres of real wood trim. Under the hood is a 6.6-liter twin-turbo V-12 that makes 563 horsepower, so the Dawn is as fleet as it is sexy.</p>
	<br>
	<p class="par">The Rolls-Royce Dawn is most at home cruising the glittering boulevards of Palm Springs or Palm Beach, the Siriusly Sinatra channel permanently playing, and the driver wearing $2600 Gucci crocodile loafers while contemplating how to cajole public financing for his new NFL stadium. Unlike, say, the similarly pricey Lambor­ghini Aventador, the Dawn can be driven and used every day. It clears curbs, there’s some room in the trunk, there is a trunk, and it’s easy to get in and out of. It also doesn’t invite a race at every stoplight, though it may invite people of all races to spontaneously come up to the window and quote ­Bernie Sanders. Alas, first-world problems. </p>
	<br>
	<p class="paragraph">
		

At a glance, the Dawn is a decapitated Wraith coupe, but Rolls says 80 percent of the body panels are unique to the new car. Top up, it has a spec­tacu­lar raked profile, and a gorgeous sailing-sloop silhouette when the six-layer cloth top is down. With its recessed grille, it looks fantastic from the front whether you choose to keep the Flying Lady ornament deployed or, as is allowed by a function in the center-console screen, hidden away below a trapdoor. From the rear, well, the car is not quite as distinctively stately.

It rides on the same 122.5-inch wheelbase as the Wraith and is only slightly longer overall. The pair shares basic suspension, drivetrain, and structural elements, with the core engineering derived from a previous-generation BMW 7-series. From a performance standpoint, the difference is that the hardtop Wraith is about 400 pounds lighter and pushes its mass around with 624 horsepower from its BMW-built twin-turbo 6.6-liter V-12. The 5776-pound Dawn’s similar V-12 is tuned to just 563 horsepower. 
	</p>
