	<p><h2>&nbsp;Overview</h2></p>
<p class="par">
KTM RC 200 Launched at Rs 1.71 Lakh (ex-showroom, Delhi). Built around the steel trellis frame, RC 200 is the entry level sports bike from KTM. Styling is similar to elder sibling RC 390 and there is a striking similarity in the design as well as the size of fairing used on 200. Lately the bike maker made significant additions to the entire range available in India. Powering the sports bike is a 200cc engine borrowed from Duke 200 that uses a six-speed gearbox.
</p>
<h3>KTM RC200 Looks</h3>
<p class="par">
Front gets twin projector headlamps, boomerang shaped DRLs with white light and a clear windscreen. The instrument cluster is similar to the one seen on Duke 200 and the all black brawny fuel tank further accentuates the muscular look. Unlike its naked sibling, the bike gets a clip-on handle-bar and the steel trellis frame coloured in orange lends RC 200 the perfect sports bike look.
</p>
<h3>KTM RC200 Engine Specifications & Transmission</h3>
<p class="par">
Power is drawn from a 199.5cc, single cylinder, engine putting out maximum power of 25bhp along with 19 Nm of torque and the power is directed to wheels via a six-speed gearbox.
KTM RC200 Features
Features on offing include twin-projector headlamps, DRLS with white light, LED tail lamps, muscular fuel tank, fat tyres, large seat, orange steel trellis frame, clip-on handle bar, Led brake lamps and LED side indicators on the ORVMs among others.</p>
<h3>KTM RC200 Braking & Handling</h3>
<p class="par">
Stopping power comes from disc units but ABS is missing from the sports bike.
KTM RC200 Wheels & Tyres

KTM has added new pattern MRF tyres to RC 200, the fat tyres offer good grip on the road.
