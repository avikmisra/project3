Bajaj Auto has silently launched the Pulsar NS160 in India, without much fanfare, a Bajaj official confirmed to Carandbike. The bike has been priced at Rs 78368 (ex-showroom, Delhi) and we believe a formal announcement will follow post 1 July, 2017 after the Goods and Services Tax (GST) kicks in. The Pulsar NS160 bike was spotted testing in India recently, which paved way for speculation about its launch. The Pulsar NS160 is already on sale in markets such as Nepal and Turkey and now it serves to fill up the gap that existed between the Pulsar 150 and the Pulsar 180.</p>
